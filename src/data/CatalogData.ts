/* -----------------------------
   Types
   ----------------------------- */

export type CatalogItem = {
  id: number;
  category: string;
  nameKey: string;
  descriptionKey: string;
  price: number;

  // main "basename" (no extension)
  imageBase: string;

  // optional additional images (also basenames)
  imageBases?: string[];

  // these are *translation keys* now (e.g. "variants.colors.blue")
  colors?: string[];
  sizes?: string[];
  // optional explicit prices per size (keys match `sizes` entries)
  // if provided, these are absolute prices (not modifiers)
  sizePrices?: Record<string, number>;
  boxInfo?: string;
};

export type ImageSources = {
  avif?: string;
  webp?: string;
  fallback: string;
};

/* -----------------------------
   Helpers for item images
   ----------------------------- */

const itemsImg = import.meta.glob(
  "@/assets/items/**/**.{png,jpg,jpeg,webp,avif}",
  {
    eager: true,
  }
) as Record<string, { default: string }>;

export const getImageSources = (basename: string): ImageSources => {
  const avif =
    itemsImg[`/src/assets/items/items_avif/${basename}.avif`]?.default;
  const webp =
    itemsImg[`/src/assets/items/items_webp/${basename}.webp`]?.default;

  const fallback = webp;

  if (!avif && !webp) {
    console.warn(`No modern image found for: ${basename}`);
  }

  return {
    avif,
    webp,
    fallback: webp || avif, // optional placeholder
  };
};

