export type ImageSources = {
  webp?: string;
  fallback: string;
};

const itemsImg = import.meta.glob(
  "/src/assets/items/**/*.{png,jpg,jpeg,webp}",
  { eager: true },
) as Record<string, { default: string }>;

export const getImageSources = (
  basename: string,
  category?: string,
): ImageSources => {
  const basePath = category
    ? `/src/assets/items/${category}/${basename}`
    : `/src/assets/items/${basename}`;

  const webp = itemsImg[`${basePath}.webp`]?.default;
  const jpg =
    itemsImg[`${basePath}.jpg`]?.default ||
    itemsImg[`${basePath}.jpeg`]?.default ||
    itemsImg[`${basePath}.png`]?.default;

  if (!webp && !jpg) {
    console.warn(`Image not found: ${basePath}`);
  }

  return {
    webp,
    fallback: webp || jpg || "",
  };
};

